/**
 * User entity, used for filtering as well.
 */
export class User {
  /**
   * @type {number} id Unique numeric identifier.
   */
  firstName: String;
  
  lastName: String;

  /**
   * @type {string} title The title of the book.
   */
  email: String;

  /**
   * @type {string} author The author of the book.
   */
  address: String;
  
}